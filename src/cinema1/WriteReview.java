    private void tIdFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tIdFieldKeyReleased
        if(!(tIdField.getText().equals(""))){
        int temp=Integer.parseInt(tIdField.getText());
        for(TicketInfo i:c.t){
            if (i.checkTicketID(temp)){
                reviewTa.setEditable(true);
                saveBtn.setEnabled(true);
                reviewTa.setToolTipText(null);
                saveBtn.setToolTipText(null);
                return;
            }
        }
        }
        reviewTa.setEditable(false);
        saveBtn.setEnabled(false);
        reviewTa.setToolTipText("Write a valid Ticket ID");
        saveBtn.setToolTipText("Write a valid Ticket ID");
    }//GEN-LAST:event_tIdFieldKeyReleased

