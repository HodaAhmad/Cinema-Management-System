    public WriteReview() {
        initComponents();
        
        String myUrl="jdbc:derby://localhost:1527/cinemaa";
        try{
            Connection conn=DriverManager.getConnection(myUrl,"cinema","cinema");
            Statement st=conn.createStatement();
            ResultSet rs=null;
            String tSql="SELECT*FROM TICKETINFO";
            rs=st.executeQuery(tSql);
            while(rs.next()){
                c.t.add(new TicketInfo( rs.getInt("TicketID"), rs.getString("seatNo"), rs.getDouble("price"),rs.getInt("screenNo"), rs.getBoolean("imax"), rs.getBoolean("vipSeat")));
            }
            rs.close();
            st.close();
            conn.close();
        }catch(SQLException ex){
                System.out.println(ex);
        }
        reviewTa.setEditable(false);
        saveBtn.setEnabled(false);
        reviewTa.setToolTipText("Write a valid Ticket ID");
        saveBtn.setToolTipText("Write a valid Ticket ID");
        
    }
    
